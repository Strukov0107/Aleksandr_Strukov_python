y_2 = 0             #количество событий 2 - на обоих рулетках выпали чётные числа

while counter < n :
    a = np.random.uniform(0, 37)
    x_1 = int(a // 1)
    a = np.random.uniform(0, 37)
    x_2 = int(a // 1)
    counter += 1
    if x_1 == 0 or x_2 == 0 :
        y_1 += 1
    if x_1 % 2 == 0 :
        if x_2 % 2 == 0:
            y_2 += 1

