import numpy as np

#пусть у нас есть две независымые рулетки
#крутим рулетки одновременно и наблюдаем
counter = 0         #   счётчик проведённых испытаний
n = 10000           #сколько необходимо провести испытаний
x_1 = 0             #число, которое выпало на рулетке 1
